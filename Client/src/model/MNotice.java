package model;

public class MNotice extends MModel {
	
	private String id;
	private String title;
	private String contents;
	private String writer;

	public MNotice() {
		
	}

	public String getId() {
		return id;
	}

	public String getTitle() {
		return title;
	}

	public String getContents() {
		return contents;
	}

	public String getWriter() {
		return writer;
	}
		
}
